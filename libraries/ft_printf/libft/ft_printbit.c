#include "libft.h"

void	ft_printbit(int n)
{
	int i;

	i = 128;
	while (i > 0)
	{
		ft_putchar(*(n >= i ? "1" : "0"));
		n = n >= i ? n % i : n;
		i /= 2;
	}
}
